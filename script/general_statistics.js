function addDonut(canvasId, dataJson, title) {
  new Chart(canvasId, {
    type: "doughnut",
    data: {
      labels: dataJson.map((item) => item.id),
      datasets: [
        {
          data: dataJson.map((item) => item.quan),
          hoverOffset: 4,
        },
      ],
    },
    options: {
      responsive: true,
      plugins: {
        title: {
          display: true,
          text: title,
        },
        legend: {
          position: "bottom",
        },
      },
    },
  });
}

function addBar(canvasId, dataJson, title) {
  new Chart(canvasId, {
    type: "bar",
    data: {
      labels: dataJson.map((item) => item.id),
      datasets: [
        {
          data: dataJson.map((item) => item.quan),
        },
      ],
    },
    options: {
      responsive: true,
      indexAxis: "y",
      plugins: {
        title: {
          display: true,
          text: title,
        },
        legend: {
          display: false,
        },
      },
      scales: {
        x: {
          type: "logarithmic",
          beginAtZero: false,
          ticks: {
            callback: function (value) {
              return Number(value.toString());
            },
          },
        },
      },
    },
  });
}

